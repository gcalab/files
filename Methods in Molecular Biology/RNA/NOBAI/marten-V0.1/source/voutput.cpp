/****************************************************************************
 *                                                                          *
 *     This function prints some verbose output.                            *
 *                                                              MARTEN 2007 *
 ****************************************************************************/

void voutput(int option)
{
  if(option == 1) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " Removes white spaces and hats     !\n"
	      <<       " ***********************************\n";
  }

  if(option == 2) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " Removes gaps in stems             !\n"
	      <<       " ***********************************\n";
  }

  if(option == 3) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " Removes non-standard pairing info  \n"
	      <<       " ***********************************\n";
  }

  if(option == 4) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " Sums the nucleotides in stems     !\n"
	      <<       " ***********************************\n";
  }

  if(option == 5) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " Sums the size of bulges           !\n"
	      <<       " ***********************************\n";
  }
 
  if(option == 6) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " Sums the size of bulges in a stem !\n"
	      <<       " ***********************************\n";
  }

  if(option == 7) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " Sums nucleotides outside []       !\n"
	      <<       " ***********************************\n";
  }

  if(option == 8) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " Eliminate gaps '-' outside '[]'.  !\n"
	      <<       " ***********************************\n";
  }

  if(option == 9) {
    std::cout << "\n\n\n ***********************************\n"
	      <<       " PAUP MATRIX GENERATION            !\n"
	      <<       " ***********************************\n";
  }
}
